#include "../../libft.h"

void	*ft_memmove(void *dest, void *src, size_t n)
{
	size_t	i;

	if (dest == NULL || src == NULL)
		return (dest);
	i = 0;
	if (dest > src && dest <= src + n)
	{
		while (n)
		{
			((unsigned char *)dest)[n - 1] = ((unsigned char *)src)[n - 1];
			n--;
		}
	}
	else
	{
		while (i < n)
		{
			((unsigned char *)dest)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	return (dest);
}
